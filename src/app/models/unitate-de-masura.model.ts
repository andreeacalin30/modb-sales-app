export class UnitateDeMasura{
    IDUnitateMasura: number;
    NumeUnitateDeMasura: string;
    Inaltime : number;
    Latime : number;
    Lungime : number;


    constructor(IDUnitateMasura: number,
        NumeUnitateDeMasura: string,
        Inaltime : number,
        Latime : number,
        Lungime : number){
            this.IDUnitateMasura=IDUnitateMasura;
            this.NumeUnitateDeMasura=NumeUnitateDeMasura
            this.Inaltime=Inaltime
            this.Latime=Latime
            this.Lungime=Lungime
    }
}